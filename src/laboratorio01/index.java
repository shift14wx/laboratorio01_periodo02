    public void llenarTabla(String tablaAllenar){ // tomaremos el nombre de la tabla a llenar
    DefaultTableModel mdTabla = new DefaultTableModel();
    int tablaEncabezadosIndex = Arrays.asList(encabezadosArray).indexOf(tablaAllenar);
    ArrayList<String>lCabeza = lEncabezados.get(tablaEncabezadosIndex);
        for (String object : lCabeza) {
            mdTabla.addColumn(object);
        }
        
        switch(tablaAllenar){
            case "Clientes":
            tableClientes.setModel(mdTabla);
            listaClientes.forEach(rows->{
                String [] rowsClientes = {
                   rows.nombre,
                   rows.dui
                };
                mdTabla.addRow(rowsClientes);
             });
            tableClientes.setModel(mdTabla);
            break;
            case "Productos":
            tableProductos.setModel(mdTabla);
            listaProductos.forEach(rows->{
                String [] rowsClientes = {
                   rows.nombre,
                   rows.precio.toString()
                };
                mdTabla.addRow(rowsClientes);
             });
            tableProductos.setModel(mdTabla);
            break;
            case "Cajeros":
            tableCajeros.setModel(mdTabla);
            listaCajeros.forEach(rows->{
                String [] rowsClientes = {
                   rows.nombre,
                   rows.sucursal
                };
                mdTabla.addRow(rowsClientes);
             });
            tableCajeros.setModel(mdTabla);
            break;
            case "Transacciones":
            tableTransacciones.setModel(mdTabla);
            listaTransacciones.forEach(rows->{
                String [] rowsClientes = {
                   rows.cliente.nombre,
                   rows.cajero.nombre,
                   rows.producto.nombre,
                   rows.precio.toString(),
                   rows.monto.toString()
                };
                mdTabla.addRow(rowsClientes);
             });
            tableTransacciones.setModel(mdTabla);
            break;
        }
    
    }
    
