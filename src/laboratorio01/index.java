    public void reformarTablaPorClienteYProducto(){
        this.lblMaximo.setText("");
        this.lblMinimo.setText("");
        this.lblTotal.setText("");
    DefaultTableModel mdTabla = new DefaultTableModel();
    ArrayList<String>lCabeza = lEncabezados.get(3);
        for (String object : lCabeza) {
            mdTabla.addColumn(object);
        }
        this.pagos = new ArrayList<Double>();
        String productoNombre = this.listaProductos.get(this.cbProductos.getSelectedIndex()).nombre;
    tableTransacciones.setModel(mdTabla);
    listaTransacciones.forEach(rows->{
        if(this.lblDui.getText().equals(rows.cliente.dui) && productoNombre.equals(rows.producto.nombre)){
        String [] rowsClientes = {
           rows.cliente.nombre,
           rows.cajero.nombre,
           rows.producto.nombre,
           rows.precio.toString(),
           rows.monto.toString()
        };
        //tomamnos los montos temporales en un arrayList
        pagos.add(rows.monto);
        mdTabla.addRow(rowsClientes);
        }
     });
    
    tableTransacciones.setModel(mdTabla);
        
    //set minimo maximo avg total de pago
    //INFORMACION  https://www.javacodeexamples.com/find-minimum-maximum-value-in-arraylist/1010
    this.lblMaximo.setText(Collections.max(pagos).toString());
    this.lblMinimo.setText(Collections.min(pagos).toString());
    this.lblStado.setText("");
    this.lblFaltante.setText("");
    this.lblAvg.setText("");
    
        this.totalTemp = 0.0;
        for (Double pago : pagos) {
            totalTemp+=pago;
        }
    this.lblTotal.setText(String.format("%.2f",totalTemp));
    Double avgTemp = totalTemp/pagos.size();
    this.lblAvg.setText(String.format("%.2f",avgTemp));
    this.faltanteTemp = Double.parseDouble(this.lblPrecio.getText())-totalTemp;
        if (faltanteTemp>=0.00) {
            this.lblFaltante.setText(String.format("%.2f",faltanteTemp));
        }else{
            this.lblFaltante.setText("0.00");
        }
        /*Double tempTt = (Double.parseDouble(this.spMonto.getValue().toString())-faltanteTemp);
        if( tempTt < 0.0 ){
            JOptionPane.showMessageDialog(null, "Devolucion de dinero: $"+tempTt);
        }*/
    if (totalTemp >= Double.parseDouble(this.lblPrecio.getText())) {
        this.lblStado.setText("FINALIZADO");
        this.lblStado.setForeground(Color.GREEN);
    }else{
        this.lblStado.setText("FALTA");
        this.lblStado.setForeground(Color.RED);
    }

    }
